// Undefined indica que no se ha incializado una variable y que el valor esta ausente.
let indefinida;
console.log(indefinida);

// Null es un valor especial que indica la ausencia de un valor
let nulo = null;
console.log(nulo);

// NaN: Not A Number
let noEsUnNumero = "Hola" * 3.7;
console.log(noEsUnNumero);